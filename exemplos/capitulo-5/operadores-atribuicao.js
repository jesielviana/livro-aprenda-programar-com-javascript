// Atribuição simples
let nome = 'Ana';
console.log('nome:', nome); // Saída => nome: Ana

// Atribuição com adição
let idade = 20;
idade += 2; 
console.log('idade:', idade); // Saída => idade: 22

// Atribuição com subtração
let saldo = 100;
saldo -= 50; // saldo = 50
console.log('saldo:', saldo); // Saída => saldo: 50

// Atribuição com multiplicação
let pontuacao = 10;
pontuacao *= 2; // pontuacao = 20
console.log('pontuacao:', pontuacao); // Saída => pontuacao: 20

// Atribuição com divisão
let tempo = 60;
tempo /= 2; // tempo = 30
console.log('tempo:', tempo); // Saída => tempo: 30

// Atribuição com resto da divisão
let resultado = 10 % 3;
resultado %= 2; // resultado = 0
console.log('resultado:', resultado); // Saída => resultado: 0
